/*============================ 基础绘图函数 ============================*/

/**
 * @brief  绘制空心矩形
 */
void ui_draw_rect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    st7789_fill_color(x, y, x + w - 1, y + 1, color);           /* 上边 */
    st7789_fill_color(x, y + h - 2, x + w - 1, y + h - 1, color); /* 下边 */
    st7789_fill_color(x, y, x + 1, y + h - 1, color);           /* 左边 */
    st7789_fill_color(x + w - 2, y, x + w - 1, y + h - 1, color); /* 右边 */
}

/**
 * @brief  绘制圆角矩形（简化：绘制普通填充矩形）
 */
void ui_draw_round_rect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    st7789_fill_color(x, y, x + w - 1, y + h - 1, color);
}

/**
 * @brief  绘制进度条
 */
void ui_draw_progress_bar(uint16_t x, uint16_t y, uint16_t w, uint16_t h,
                          uint8_t percent, uint16_t color)
{
    if(percent > 100) percent = 100;

    /* 背景（深灰色）*/
    ui_draw_round_rect(x, y, w, h, 0x2104);

    /* 进度 */
    uint16_t filled_w = (w * percent) / 100;
    if(filled_w > 2)
    {
        ui_draw_round_rect(x + 1, y + 1, filled_w - 2, h - 2, color);
    }
}

/**
 * @brief  绘制单个字符（8x8点阵）
 */
static void ui_draw_char(uint16_t x, uint16_t y, char ch, uint16_t color)
{
    if(ch < 32 || ch > 127) return;

    const uint8_t *font_data = font_8x8[(uint8_t)ch];

    for(uint8_t row = 0; row < 8; row++)
    {
        uint8_t line = font_data[row];
        for(uint8_t col = 0; col < 8; col++)
        {
            if(line & (0x80 >> col))
            {
                st7789_draw_pixel(x + col, y + row, color);
            }
        }
    }
}

/**
 * @brief  绘制数字
 */
void ui_draw_number(uint16_t x, uint16_t y, int32_t num, uint16_t color)
{
    char buf[12];
    snprintf(buf, sizeof(buf), "%d", (int)num);
    ui_draw_string(x, y, buf, color);
}

/**
 * @brief  绘制字符串
 */
void ui_draw_string(uint16_t x, uint16_t y, const char *str, uint16_t color)
{
    uint16_t cur_x = x;
    while(*str)
    {
        ui_draw_char(cur_x, y, *str, color);
        cur_x += 8;  /* 8像素字符宽度 */
        str++;
    }
}

/**
 * @brief  绘制水平线
 */
void ui_draw_line_h(uint16_t x, uint16_t y, uint16_t len, uint16_t color)
{
    st7789_fill_color(x, y, x + len - 1, y, color);
}
